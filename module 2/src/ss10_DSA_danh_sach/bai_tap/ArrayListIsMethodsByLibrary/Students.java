package ss10_DSA_danh_sach.bai_tap.ArrayListIsMethodsByLibrary;

public class Students {
    private int id;
    private String name;

    public Students() {
    }

    public Students(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
